// 1. Arithmetic Operators
// These operators are used to perform mathematical operations.

// + (Addition): Adds two operands (e.g., 5 + 3 results in 8).
// console.log(5 * 3);

// - (Subtraction): Subtracts the right operand from the left operand (e.g., 5 - 3 results in 2).
// * (Multiplication): Multiplies two operands (e.g., 5 * 3 results in 15).
// / (Division): Divides the left operand by the right operand (e.g., 6 / 3 results in 2).
// % (Modulus): Returns the remainder when the left operand is divided by the right operand (e.g., 5 % 2 results in 1).
// ** (Exponentiation): Performs exponentiation (e.g., 2 ** 3 results in 8).

// 2. Assignment Operators
// These operators assign values to variables.
// =: Assigns the value of the right operand to the left operand (e.g., let x = 10).
console.log(5 == 5);


// +=: Adds the right operand to the left operand and assigns the result to the left operand (e.g., x += 5 is equivalent to x = x + 5).
// -=: Subtracts the right operand from the left operand and assigns the result to the left operand (e.g., x -= 5 is equivalent to x = x - 5).
// *=: Multiplies the left operand by the right operand and assigns the result to the left operand (e.g., x *= 5).
// /=: Divides the left operand by the right operand and assigns the result to the left operand (e.g., x /= 5).
// %=: Assigns the remainder of the division of the left operand by the right operand to the left operand (e.g., x %= 5).
// 3. Comparison Operators
// These operators are used to compare two values and return a Boolean value (true or false).

// ==: Equal to (compares values only, ignoring type) (e.g., 5 == '5' is true).
// ===: Strictly equal to (compares both value and type) (e.g., 5 === '5' is false).
// !=: Not equal to (compares values only) (e.g., 5 != '5' is false).
// !==: Strictly not equal to (compares both value and type) (e.g., 5 !== '5' is true).
// >: Greater than (e.g., 5 > 3 is true).
// <: Less than (e.g., 3 < 5 is true).
// >=: Greater than or equal to (e.g., 5 >= 5 is true).
// <=: Less than or equal to (e.g., 3 <= 5 is true).
// 4. Logical Operators
// These operators are used to combine or negate Boolean values.

